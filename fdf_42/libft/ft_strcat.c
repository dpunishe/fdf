#include "includes/libft.h"

char	*ft_strcat(char *s2, const char *s1)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (s2[i] != '\0')
		i++;
	j = 0;
	while (s1[j] != '\0')
	{
		s2[i + j] = s1[j];
		j++;
	}
	s2[i + j] = '\0';
	return (s2);
}
